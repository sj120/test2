package practice;

import java.util.Scanner;
 // 정수 한 개를 입력받아서, 그 수가 50 이상의 수인지 50미만의 수인지 판단해보자.
public class pr_2 {
 
	public static void main(String[] args) {
		Scanner sc = new Scanner(system.in);
		
		int num = sc.nextInt();
		
       if (num>=50) System.out.println("50 이상");
       else System.out.println("50 미만");
	}

}
